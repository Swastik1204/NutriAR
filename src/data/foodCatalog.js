export const FOOD_BY_SLUG = Object.fromEntries(FOOD_CATALOG.map((food) => [food.slug, food]));
export const FOOD_BY_TARGET_INDEX = Object.fromEntries(FOOD_CATALOG.map((food) => [Number(food.imageTargetIndex), food]));

export function getFoodBySlug(slug) {
  return FOOD_BY_SLUG[String(slug || '').trim().toLowerCase()] ?? null;
}

export function getFoodByTargetIndex(index) {
  return FOOD_BY_TARGET_INDEX[Number(index)] ?? null;
}

export default FOOD_CATALOG;
